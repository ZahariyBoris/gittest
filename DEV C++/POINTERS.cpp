#include <iostream>

int main() {
	setlocale(LC_ALL, "UKRAINIAN");
	
	// Створюємо змінну
	int a = 7;
	
	// Створюємо вказівник ptr і робимо взяття змінної А
	int *ptr = &a;
	
	// Змінюємо значення у змінній А за допомогою вказівника ptr
	*ptr = 3;
	
	// Виводимо змінну А з новим значенням всередині
	std::cout << "Змінна А: " << a;
	
	return 0;
}
